public class If_else {
    public static void main(String args[]) {
        int a = 8;
        int b = 7;
        if (a == b) {
            System.out.println("isequal");
        } else {
            if (a > b) {
                System.out.println("is greater");
            } else {
                System.out.println("is less");
            }
        }
    }

}
